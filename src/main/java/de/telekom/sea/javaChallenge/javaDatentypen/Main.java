package de.telekom.sea.javaChallenge.javaDatentypen;

public class Main {

	public static void main(String[] args) {

		Primitiven_Datentypen();

	}

	public static void Primitiven_Datentypen() {
		System.out.println("Typ:	Standardwert:	Größe:");
		System.out.println("boolean	false   	1 bit");
		System.out.println("byte	0	   	1 byte");
		System.out.println("char	\\u0000   	2 byte");
		System.out.println("short	0		2 byte");
		System.out.println("int	0	 	4 byte");
		System.out.println("float	0.0f       	4 byte");
		System.out.println("long	0L    		8 byte");
		System.out.println("double	0.0d	   	8 byte");
	}

}
